use proconio::input;

fn main() {
    input! {
        x: i32,
    }
    let num = match x {
        400..=599 => 8,
        600..=799 => 7,
        800..=999 => 6,
        1000..=1199 => 5,
        1200..=1399 => 4,
        1400..=1599 => 3,
        1600..=1799 => 2,
        1800..=1999 => 1,
        _ => unreachable!(),
    };
    println!("{}", num);
}